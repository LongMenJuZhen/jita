# Create a minimal 16x16 ICO file
$iconData = @(
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x10, 0x10, 0x00, 0x00, 0x01, 0x00, 0x20, 0x00, 0x68, 0x04, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00
)
# BMP header (40 bytes)
$bmpHeader = @(
    0x28, 0x00, 0x00, 0x00,  # header size
    0x10, 0x00, 0x00, 0x00,  # width
    0x20, 0x00, 0x00, 0x00,  # height (doubled for ICO)
    0x01, 0x00,              # planes
    0x20, 0x00,              # bits per pixel
    0x00, 0x00, 0x00, 0x00,  # compression
    0x00, 0x04, 0x00, 0x00,  # image size
    0x00, 0x00, 0x00, 0x00,  # x pixels per meter
    0x00, 0x00, 0x00, 0x00,  # y pixels per meter
    0x00, 0x00, 0x00, 0x00,  # colors used
    0x00, 0x00, 0x00, 0x00   # important colors
)
# Create 32-bit BGRA pixel data (16x16 * 4 bytes = 1024 bytes)
$blue = @(0xD4, 0x07, 0x00, 0xFF)  # #0078D4
$white = @(0xFF, 0xFF, 0xFF, 0xFF)
$pixels = New-Object byte[] 1024
for ($i = 0; $i -lt 256; $i++) {
    $pixels[$i * 4] = $blue[0]
    $pixels[$i * 4 + 1] = $blue[1]
    $pixels[$i * 4 + 2] = $blue[2]
    $pixels[$i * 4 + 3] = $blue[3]
}
# AND mask (16x16 bits = 32 bytes per row, 16 rows = 64 bytes total)
$mask = New-Object byte[] 64

$all = $iconData + $bmpHeader + $pixels + $mask
[System.IO.File]::WriteAllBytes("icons/icon.ico", [byte[]]$all)
Write-Host "Icon created"
